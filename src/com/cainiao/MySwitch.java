package com.cainiao;

public class MySwitch {
    public static void main(String[] args){

        String s = "hello";

        switch (s){
            case "h":
                System.out.println("h");
                break;
            case "hello":
                System.out.println("hello");
                break;
            default:
                System.out.println("default");

        }
    }
}
